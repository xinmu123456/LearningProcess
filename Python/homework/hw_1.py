import turtle as tl

def draw_circle(x, y, color="blue") -> None:
    tl.pu()
    tl.speed(0)
    tl.setpos(x, y)
    tl.width(10)
    tl.pendown()
    tl.color(color)
    tl.circle(80)
    
def draw_five_circle():
    pos_list: list = [(-150, 100, "blue"), (-150 + 185, 100, "black"), (-150 + 185 + 185, 100, "red"), (-50, 0, "yellow"), (-50 + 185, 0, "green")]
    
    for p in pos_list:
        draw_circle(*p)

